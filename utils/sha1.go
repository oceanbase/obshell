package utils

import (
	"crypto/sha1"
	"encoding/hex"
)

func Sha1(input string) string {
	hasher := sha1.New()
	hasher.Write([]byte(input))
	hashBytes := hasher.Sum(nil)
	hashString := hex.EncodeToString(hashBytes)
	return hashString
}
